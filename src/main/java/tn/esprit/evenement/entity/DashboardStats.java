package tn.esprit.evenement.entity;

public class DashboardStats {
    private int totalEvenements;
    private int totalOuverts;
    private int totalFermes;
    private int totalPlanifies;
    private int totalParticipations;
    private int totalMesEvenements;
    private int totalParticipantsMesEvenements;

    public int getTotalEvenements() { return totalEvenements; }
    public void setTotalEvenements(int totalEvenements) { this.totalEvenements = totalEvenements; }
    public int getTotalOuverts() { return totalOuverts; }
    public void setTotalOuverts(int totalOuverts) { this.totalOuverts = totalOuverts; }
    public int getTotalFermes() { return totalFermes; }
    public void setTotalFermes(int totalFermes) { this.totalFermes = totalFermes; }
    public int getTotalPlanifies() { return totalPlanifies; }
    public void setTotalPlanifies(int totalPlanifies) { this.totalPlanifies = totalPlanifies; }
    public int getTotalParticipations() { return totalParticipations; }
    public void setTotalParticipations(int totalParticipations) { this.totalParticipations = totalParticipations; }
    public int getTotalMesEvenements() { return totalMesEvenements; }
    public void setTotalMesEvenements(int totalMesEvenements) { this.totalMesEvenements = totalMesEvenements; }
    public int getTotalParticipantsMesEvenements() { return totalParticipantsMesEvenements; }
    public void setTotalParticipantsMesEvenements(int totalParticipantsMesEvenements) { this.totalParticipantsMesEvenements = totalParticipantsMesEvenements; }
}
